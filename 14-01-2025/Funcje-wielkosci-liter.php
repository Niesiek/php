    <p><b>mb_strtoupper(argument, [kodowanie])</b></p><br>
    <p><b>mb_strtolower(argument, [kodowanie])</b></p><br>
    <p><b>[kodowanie]</b> to sposób kodowania znaków</p>
    <h2>Kolejnymi  fukcjami są:</h2>
    <p><b>ucfirst(argument) oraz ucwords(argument)</b></p>
    <p>Funckja <b>ucfirst</b> zmodyfikuje podany argument w ten sposób, ze pierwsza literaz ciągu zostanie zamieniona na duza litere</p><br>
    <p>Funckja <b>ucowrds</b> zmodyfikuje ciąg znaków jako argument w ten sposób, e wszystkie pierwsze litery wyrazów zostaną zamienione na duze litery</p>
